    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here:
        gl.setName(jTextFieldName.getText());
        gl.setType(jTextFieldType.getText());
        gl.setCategories(jTextFieldCategories.getText());
        gl.setExec(jTextFieldExec.getText());
        gl.setIcon(jTextFieldIcon.getText());
        gl.setOutput(jTextFieldOutput.getText());
        Charset charset = Charset.forName(StandardCharsets.UTF_8.name());
        String s = "[Desktop Entry]\n"
                + "Name=" + gl.getName() + newline
                + "Exec=" + gl.getExec() + newline
                + "Icon=" + gl.getIcon() + newline
                + "Type=" + gl.getType() + newline
                + "Categories=" + gl.getCategories() + newline;
        File file = new File(gl.getOutput());
        try (BufferedWriter writer = Files.newBufferedWriter(file.toPath(), charset)) {
            writer.write(s, 0, s.length());
            jTextAreaLog.append("The .desktop file was created: " + gl.getOutput() + newline);
            jTextAreaLog.setCaretPosition(jTextAreaLog.getDocument().getLength());
        } catch (IOException x) {
            System.err.format("IOException: %s%n", x);
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

