    private void jButtonBrowseExecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBrowseExecActionPerformed
        // TODO add your handling code here:
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            //This is where a real application would open the file.
            gl.setExec(file.getAbsolutePath());
            jTextFieldExec.setText(file.getAbsolutePath());
        } else {
            jTextAreaLog.append("browse Exec command cancelled by user." + newline);
        }
        jTextAreaLog.setCaretPosition(jTextAreaLog.getDocument().getLength());
    }//GEN-LAST:event_jButtonBrowseExecActionPerformed

